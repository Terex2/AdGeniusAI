from app.utils.llm_handler import LLMHandler

class AnalyticsAgent:
    def __init__(self):
        self.llm = LLMHandler()

    async def process(self, campaign_data: dict) -> str:
        prompt = f"""
        أنت محلل بيانات تسويقية (Marketing Data Analyst) وخبير في تحسين معدلات التحويل (CRO).

        المطلوب منك هو تحليل خطة الحملة والمخرجات التالية وتقديم رؤى نقدية وتوصيات للتحسين:
        بيانات الحملة المقترحة:
        {campaign_data}
        
        المطلوب تقديم تقرير تحليلي يشمل:
        1. **تقييم التماسك (Consistency Check)**: هل تتوافق النصوص والتصاميم مع أبحاث السوق والجمهور المستهدف؟
        2. **تحديد المخاطر المحتملة**: ما هي العقبات التي قد تواجه هذه الحملة؟
        3. **توصيات التحسين الفوري**: كيف يمكن جعل الرسائل أكثر قوة؟
        4. **استراتيجية الاختبار (A/B Testing)**: ما هي العناصر التي يجب اختبارها أولاً؟
        5. **توقعات الأداء**: بناءً على المعايير الصناعية، ما هو الأداء المتوقع؟

        اكتب التحليل باللغة العربية بأسلوب نقدي بناء وعلمي. استخدم تنسيق Markdown.
        """
        return await self.llm.get_response(prompt)
